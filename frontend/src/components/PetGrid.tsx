import { useState, useEffect } from "react";
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select";
import ProductCard from "./ProductCard";
import Breadcrumb from "./Breadcrumb";
import { useProductTypes, useCategories } from "@/hooks/useApi";
import { Product, ProductType, Category } from "@/types";
import { productService } from "@/services/productService"; // üÜï import tr·ª±c ti·∫øp service

const PetGrid = () => {
  const [products, setProducts] = useState<Product[]>([]);
  const [sortBy, setSortBy] = useState("default");
  const [selectedType, setSelectedType] = useState<string>("all");
  const [selectedCategory, setSelectedCategory] = useState<string>("all");
  const [filteredProducts, setFilteredProducts] = useState<Product[]>([]);
  const [loading, setLoading] = useState(true);

  //  L·∫•y lo·∫°i s·∫£n ph·∫©m & danh m·ª•c
  const { productTypes, loading: typesLoading } = useProductTypes();
  const { categories, loading: categoriesLoading } = useCategories();

  //  L·∫•y danh s√°ch s·∫£n ph·∫©m public (kh√¥ng c·∫ßn ƒëƒÉng nh·∫≠p)
  useEffect(() => {
    const fetchProducts = async () => {
      try {
        setLoading(true);
        const data = await productService.getAllProductsCustomer(); // üÜï d√πng API customer
        setProducts(data);
      } catch (error) {
        console.error("L·ªói t·∫£i s·∫£n ph·∫©m:", error);
      } finally {
        setLoading(false);
      }
    };

    fetchProducts();
  }, []);

  const breadcrumbItems = [
    { label: "TRANG CH·ª¶", href: "/" },
    { label: "S·∫¢N PH·∫®M" },
  ];

  // üß† L·ªçc & s·∫Øp x·∫øp khi d·ªØ li·ªáu thay ƒë·ªïi
  useEffect(() => {
    filterAndSortProducts();
  }, [products, sortBy, selectedType, selectedCategory]);

  const filterAndSortProducts = () => {
    let filtered = [...products];

    // L·ªçc theo lo·∫°i
    if (selectedType !== "all") {
      filtered = filtered.filter(
        (p) => p.type_id === parseInt(selectedType)
      );
    }

    // L·ªçc theo danh m·ª•c
    if (selectedCategory !== "all") {
      filtered = filtered.filter(
        (p) => p.category_id === parseInt(selectedCategory)
      );
    }

    // S·∫Øp x·∫øp
    switch (sortBy) {
      case "price-low-high":
        filtered.sort((a, b) => a.price - b.price);
        break;
      case "price-high-low":
        filtered.sort((a, b) => b.price - a.price);
        break;
      case "name-a-z":
        filtered.sort((a, b) =>
          a.product_name.localeCompare(b.product_name)
        );
        break;
      case "name-z-a":
        filtered.sort((a, b) =>
          b.product_name.localeCompare(a.product_name)
        );
        break;
    }

    setFilteredProducts(filtered);
  };

  const totalLoading = loading || typesLoading || categoriesLoading;

  if (totalLoading) {
    return (
      <div className="flex-1 p-6">
        <div className="max-w-7xl mx-auto">
          <div className="flex justify-center items-center h-64">
            <div className="text-lg">ƒêang t·∫£i...</div>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="flex-1 p-6">
      <div className="max-w-7xl mx-auto">
        <Breadcrumb items={breadcrumbItems} />

        <div className="flex justify-between items-center mb-6">
          <h1 className="text-2xl font-bold text-foreground">S·∫¢N PH·∫®M</h1>

          <div className="flex gap-4">
            {/* L·ªçc theo lo·∫°i s·∫£n ph·∫©m */}
            <Select value={selectedType} onValueChange={setSelectedType}>
              <SelectTrigger className="w-48">
                <SelectValue placeholder="Ch·ªçn lo·∫°i s·∫£n ph·∫©m" />
              </SelectTrigger>
              <SelectContent>
                <SelectItem value="all">T·∫•t c·∫£ lo·∫°i</SelectItem>
                {productTypes.map((type) => (
                  <SelectItem
                    key={type.type_id}
                    value={type.type_id.toString()}
                  >
                    {type.type_name}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>

            {/* L·ªçc theo danh m·ª•c */}
            <Select
              value={selectedCategory}
              onValueChange={setSelectedCategory}
            >
              <SelectTrigger className="w-48">
                <SelectValue placeholder="Ch·ªçn danh m·ª•c" />
              </SelectTrigger>
              <SelectContent>
                <SelectItem value="all">T·∫•t c·∫£ danh m·ª•c</SelectItem>
                {categories.map((category) => (
                  <SelectItem
                    key={category.category_id}
                    value={category.category_id.toString()}
                  >
                    {category.category_name}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>

            {/* S·∫Øp x·∫øp */}
            <Select value={sortBy} onValueChange={setSortBy}>
              <SelectTrigger className="w-48">
                <SelectValue placeholder="S·∫Øp x·∫øp" />
              </SelectTrigger>
              <SelectContent>
                <SelectItem value="default">M·∫∑c ƒë·ªãnh</SelectItem>
                <SelectItem value="price-low-high">
                  Gi√°: Th·∫•p ƒë·∫øn cao
                </SelectItem>
                <SelectItem value="price-high-low">
                  Gi√°: Cao ƒë·∫øn th·∫•p
                </SelectItem>
                <SelectItem value="name-a-z">T√™n: A ƒë·∫øn Z</SelectItem>
                <SelectItem value="name-z-a">T√™n: Z ƒë·∫øn A</SelectItem>
              </SelectContent>
            </Select>
          </div>
        </div>

        {/* Danh s√°ch s·∫£n ph·∫©m */}
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-6">
          {filteredProducts.map((product) => (
            <ProductCard
              key={product.product_id}
              product={product}
              onClick={() => {
                console.log("Clicked product:", product.product_name);
              }}
            />
          ))}
        </div>

        {products.length === 0 && (
          <div className="text-center py-12">
            <p className="text-muted-foreground text-lg">
              Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m n√†o
            </p>
          </div>
        )}
      </div>
    </div>
  );
};

export default PetGrid;
